package model.sound;

public class SoundSink {
    // The sound sink is a single point
    double x;
    double y;
    double z;

    // Constructor
    public SoundSink () {
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }
}
